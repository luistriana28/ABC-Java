                    + "c.direccion,c.edad,c.tel, "
                    + "CONCAT(i.nombre,' ',i.apellidos), cl.nombre from clientes c "
                    + "inner join instructor i on "
                    + "c.instructor_id=i.instructor_id INNER JOIN "
                    + "clase cl on c.clase_id=cl.clase_id where "
                    + "c.instructor_id=i.instructor_id";
